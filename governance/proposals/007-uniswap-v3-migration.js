module.exports = async () => {
  // parse call data for function call
  const poolV2 = await ethers.getContractAt(v2PoolAbi, udtWethV2poolAddress)

  const [token0, token1, [reserve0, reserve1]] = await Promise.all([
    poolV2.token0(),
    poolV2.token1(),
    poolV2.getReserves(),
  ])
  console.log(token0, token1, reserve0, reserve1)
  const liquidity = await poolV2.balanceOf(timelockAddress)
  console.log(liquidity)

  const minTick = await getMinTick(fee)
  const maxTick = await getMaxTick(fee)

  const currentBlock = await ethers.provider.getBlockNumber()

  const migrationArgs = {
    pair: udtWethV2poolAddress,
    liquidityToMigrate: liquidity,
    percentageToMigrate: 100,
    token0,
    token1,
    fee,
    tickLower: minTick,
    tickUpper: maxTick,
    amount0Min: reserve0,
    amount1Min: reserve1,
    recipient: timelockAddress,
    deadline: currentBlock + 1000,
    refundAsETH: false,
  }

  console.log(migrationArgs)

  //migrator instance
  const migrator = await ethers.getContractAt(migratorABI, migratorAddress)

  // approve migrator to manipulate pool tokens
  await impersonate(timelockAddress)
  const signer = await ethers.getSigner(timelockAddress)
  await poolV2.connect(signer).approve(migrator.address, liquidity)

  // migrate the tokens
  await migrator.connect(signer).migrate(migrationArgs)

  const calls = []
  const proposalName = `Migrating Uniswap v2 pool to v3
  `
  console.log(proposalName)
  // send to multisig / DAO
  return {
    proposalName,
    calls,
  }
}
